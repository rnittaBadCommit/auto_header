#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t len)
{
	if (!*s2)
		return ((char *)s1);
	while (*s1 && len >= ft_strlen(s2))
	{
		if (ft_strncmp(s1, s2, ft_strlen(s2)) == 0)
			return ((char	*)s1);
		s1++;
		len--;
	}
	return (NULL);
}
