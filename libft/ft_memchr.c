#include "libft.h"

void	*ft_memchr(const void *buf, int ch, size_t n)
{
	while (n > 0)
	{
		if (*(unsigned char *)buf == (unsigned char)ch)
			return ((void *)buf);
		n--;
		buf++;
	}
	return (0);
}
