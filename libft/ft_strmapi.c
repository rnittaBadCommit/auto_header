#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*new;
	int		i;

	i = 0;
	if (!s || !*f)
		return (0);
	if (!(new = (char *)malloc(sizeof(char) * (ft_strlen(s) + 1))))
		return (0);
	while (s[i])
	{
		new[i] = (*f)((unsigned int)i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
