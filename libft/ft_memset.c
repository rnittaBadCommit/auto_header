#include "libft.h"

void		*ft_memset(void *buf, int ch, size_t n)
{
	void	*tmp;

	tmp = buf;
	while (n-- > 0)
		*(unsigned char *)buf++ = (unsigned char)ch;
	return (tmp);
}
